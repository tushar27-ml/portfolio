                            <p>Many times we don’t take decisions by thinking what’s rational. We tend to believe our instincts and become biased towards them. Similarly, when it comes to investing or handling finance, people get influenced by their own biases and instincts leaving behind rationality. This is called behavioral finance in the world of economics.</p>
                            <p>Behavioral finance existed before people named it. Way before this term was coined people used to invest their money or resources on the basis of someone’s goodwill or their own biases. Recently behaviorists and financial theorists have started to research this topic more deeply. It is believed that behavioral finance is the combination of social sciences and business studies. It includes the fields of psychology, sociology, anthropology, economics, behavioral economics, management, marketing, finance, technology and accounting.</p>
                            <p>The concept of behavioral finance stands upon two building blocks i.e. cognitive psychology and the limits to arbitrage. The way people think is sometimes unpredictable. Sometimes they are confident about the choices they make and ignore the possibility of loss or chances of risk. Sometimes people are so driven by the fear of loss that it restrains them to notice the profit they are getting out of their investment. Here cognitive psychology comes into play and it defines how people think. Heuristics, overconfidence, mental accounting, framing, representativeness, conservatism, disposition effect are some of the patterns which define cognitive psychology. </p>
                            <p>On the other hand, the limits to arbitrage is a concept which concerns the valuation and misvaluation of assets. Misvaluation of financial assets is common but can you make profit of it? It’s not easy but you can. Misvaluation is of two types: Misvaluation which are recurrent and misvaluation which are non-repeating and long term in nature. In the short term misvaluation trading strategies can reliably make money, but from the long term perspective it is impossible to identify the peaks and troughs in real time until they have passed. </p>
                            <p>Let’s understand the concept of behavioral finance with the help of an example: Suppose a lawsuit is filed against the owner of a company which is concerned with the personal life of the owner and has nothing to do with the company. Investors know from their past experience that the news of the lawsuit will make the company's share price fall. Many investors sell their holdings of shares of that particular company causing more decline in the asset’s value. Consequently, the price of shares of other companies in the same industry also falls without any tangible reason. This is a pure example of behavioral finance.

                            </p>
                            <p><img src="img/Behavioral Finance - 2.jpg"></p>
                            <h4>
                                ADVANTAGES
                            </h4>
                            <p>The concept of herding which is a pattern of cognitive psychology is the most useful market characteristic for long time investors. It’s a safer option as most of the people imitate others and invest their money where they are going to get good returns.      </p>
                            <p>Cognitive biases are also a good strategy for those who have done their homework and have enough experience to trust their instincts.

                                Sometimes going with the flow also serves you better. Following the market sentiment and going with the trend of the market is a better option to earn good profits.
                                </p>
                                <h4>DISADVANTAGES</h4>
                                <p>Following your instincts doesn’t always serve you better. Market is as volatile as human thinking and being biased during the volatility of the market doesn’t pay well. It affects our decisions. Biased thinking can negatively affect financial decisions. If you are not aware of the basic concepts of economics and business studies, behavioral finance is just a misconception for you.

                                </p>
                                <h4>CRITICISM</h4>
                                <p>The concept of behavioral finance has faced solid criticism. An American economist who is well known for his empirical work on portfolio theory and asset pricing said “Behavioral finance is a collection of anomalies”. He contended that individual cognitive biases are different from those of social biases. He also stated that the concept of behavioral finance acts as a complement to general economics and is no different than the concept of economics. It is counted both in business studies and social sciences as it contains both psychology and economics. But many economists deny it being a standalone concept and refer to it as a concept of anomalies.</p>
                            <p><img src="img/Behavioral Finance - 3.jpg"></p>
                            <h4>CONCLUSION</h4>
                            <p>Behavioral finance, it is still in its infancy. For example, individuals who shorted Japanese stocks in 1987-1988 when they were substantially overvalued lost enormous amounts of money as these stocks became even more overvalued. Behavioral finance is not a separate stream. It acts as an expression for mainstream logic and reasoning. To gain success in your investments you will have to learn both.</p>
                            <p>Believing blindly on our instincts and on our biases is not a very smart move. Even a small misvaluation of an asset can make a huge impact. The only thing which saves us from financial losses is knowledge and experience. The more more we gain knowledge, the lesser are the chances of falling in the pit of loss. Awareness plays a key role in making investments because sometimes our biases deviate us from doing the right thing. 

                            </p>
