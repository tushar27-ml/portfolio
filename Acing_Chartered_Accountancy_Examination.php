                                <h4>THE CHALLENGE</h4>
                                <p>Chartered Accountancy continues to be one of the toughest professional courses in India. Academics has always been my forte, but the challenges I encountered while pursuing this course significantly pushed me out of my comfort zone</p>
                                <blockquote>
                                    Preparing for the CA examinations is very different from studying for any other school or university exam. Every student appearing for the intermediate or the final level has 8 subjects to clear. Every subject has several books comprising of the study material, practice manual, reference notes, amendments, revision test papers, mock test papers etc.    
                                </blockquote>

                            </p>
                                   <h4> IN SHORT, A LOT OF STUDYING! </h4><p>
                                    Here smart work comes into play. Remember you are studying for one of the toughest exams in the world. Only hard work and spending hours at your study table is not going to make it work. You need to have an elaborate strategy and intricate plan of action to ace the CA examinations.
                                    <br><br>
                                    This is only possible when you know your course work and exam structure well. Now that I have crossed the river onto the other side, I would like to share a few things with the current students which they should definitively give due consideration to when preparing for the CA examinations.
                                    
                                      
                                </p>
                                <img src="img/CA - 2.png">
                                <h4>ONE GROUP v/s BOTH GROUPS.</h4>
                                <p>The first and foremost decision any CA student has to take is whether to attempt one group or both groups. Each individual group consist of 4 subjects and you have to secure a minimum of 40 marks in each and an aggregate of 50% marks overall in the group to successfully pass.
                                    <blockquote>
                                        You have an option to either attempt one group at a time or both together, which is a decision very personal to every CA student depending on his/her level of preparation for the exams and the number of days in hand.
                                    </blockquote>
                                </p>
                                <p>
                                    Some of the key factors every student should take into consideration are:<br><br>

1. By attempting both groups together you have the advantage to set off your marks. <br>
For example: If you fail to secure an aggregate of 50% marks in any one group you can carry forward the excess marks (if any) in the other group to compensate for the same. 
So when you attempt both groups your target is scoring an individual 40% in every subject and an overall 50% in both groups, which gives you a safeguard in case you fail to secure a 50% in any one particular group. 
<br><br>
2. The level of preparation and the number of days you have in hand for the exams. <br>
As all of us are serving the fag end of our articleship period when we register for our first attempt of CA Final, it is very important to take into consideration the amount of leave we get from office. 
While some students are lucky to get a 5 month long study leave, most of the others, especially the students who have opted for industrial training sometimes have less than 3 months in hand.
<br><br>
Here it is very important to clearly analyse how far your preparation has come and the how prepared you are to take the CA Final exams in the next few weeks. When you take this decision please be very practical and true to yourself.

                                </p>
                                <h4>STUDY PLAN</h4>
                                <p>Though preparation for the CA Final exams starts much before through ongoing classes during articleship, 90% of your result depends on the smart work you do in the 4 months before the exams. Likewise is the situation for students appearing for the intermediate examinations who get roughly 11 months in total to prepare.
                                    <blockquote>
                                        These last few weeks before the main exam are the most crucial, and no matter how well prepared or on target your study schedule was, messing around during this phase can surely cost you an attempt. You will have to cut down on social life and have a very structured and disciplined approach with definitive deadlines to sufficiently study and revise your entire course material
                                     </blockquote>
                                </p>
                                <p>
                                    There are a few myths though that I would like to break here:<br><br>

                                    1. Number of Hours of studying<br>
                                    More than studying for an “x” number of hours, the quality and productivity with which you study is very important. I know people who can study for 16 hours a day and I know others who can reproduce the same output within 8 hours. So don’t think you are not doing enough because you are studying for 10 hours a day while your friend is clocking 16 hours. 
                                     <br><br>
                                     2. Number of Revisions<br>This is something again very personal to your exam preparation. More than taking pride on the number of times you read the same book, it is important to gauge how confident you are with your conceptual clarity and theoretical framework. At the end of the day if your concepts are clear then that will help you crack the exams and not the number of revisions you did.  <br>
<br>
                                     3. Selecting the right elective subject<br>
                                     If you are a CA Final student and have opted for the new scheme you have to choose an elective subject from a list of available options. Please do not fall prey to the herd mentality. Read the previous sentence twice. Just because your professor recommended a subject or your friends circle opted for any particular subject do not be encouraged to select the same.

<br>
<blockquote>
    Take your own sweet time to make your choice. A wise decision here can work wonders and help you safely pass your second group! Carefully go through the content of every subject, the volume of the material, and your skills in dealing with the type of case studies tested for that particular subject.
<br>
Fair warning: Don’t think a 4 hour window is sufficient to complete the paper, because trust me it is not.

</blockquote>
                                     </p>
                                     <img src="img/CA - 3.png">
                                     <h4>EXAM WEEK</h4>
                                     <p>Finally it is exam week! Trust in your preparation. Do not panic because it does no good. Make sure your entire study material is organized for every subject so that you do not waste any time in hunting for it in between back to back exam papers. One golden rule to follow: DO NOT discuss the questions with absolutely anybody (includes parents and tutor) after any paper.
                                         <blockquote>
                                            There is no way you can change what you have already written in your answer sheet, but you sure have complete control on preparing for the next paper. So purely focus upon that. Don’t let a particularly difficult paper discourage you. It’s a tough course, the passing percentage is consistently low so there is no doubt that ICAI is going to set challenging question papers for you. 
                                         </blockquote>
                                     </p>
                                     <h4>GET IN TOUCH!</h4>
                                     <p>Make sure amidst all this chaos you eat and sleep as per your schedule. Remember, all you have to do is put your best foot forward! After reading this article, if anyone needs any further specific advice with respect to their CA exam preparations, I will be more than happy to help.
                                     </p>
